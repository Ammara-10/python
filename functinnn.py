def sum(a,b):
    """addition function"""
    return a+b
def subtract(a,b):
    """subtraction function """
    return a-b
def multiply(a,b):
    """multiplication function"""
    return a*b
def divide(a,b):
   """division function"""
   if b!=0:
     return a/b
   else:
     return 'infinite value'
